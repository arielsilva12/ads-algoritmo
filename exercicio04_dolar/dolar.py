#Ler o valor do dólar e um valor em dólar, calcular e escrever o equivalente em reais em real (R$)

#Entrada
valor_dolar = float(input("Digite o valor do dólar: "))
valor_em_dolar = float(input("Digite o valor em dólar: "))

#Processamento
valor_reais = valor_em_dolar * valor_dolar

#Saída
print(f"O equivalente em reais é: R$ {valor_reais}")


      